%% initialization
close all;
clear;
clc;

%% directory settings
root_path = fileparts(pwd);
data_path = fullfile(root_path,'data');
data_dir = dir(data_path);
data_dir = data_dir(cellfun(@(x)~contains(x,'.'),{data_dir.name}));

%% mouse settings
mouse_ids = {data_dir.name};
mouse_ids = mouse_ids(~ismember(mouse_ids,'HJ_FP_M8'));
n_mice = numel(mouse_ids);

%% experiment settings
experiment_id = 'pavlovian';
subexperiment_id = 'acquisition';
cs_dur = 2;
trace_dur = 1;
csus_delay = cs_dur + trace_dur;

%% acquisition settings
fs = 120;
dt = 1 / fs;

%% smoothing kernels
lickrate_kernel = gammakernel('peakx',.15,'binwidth',dt);

%% analysis parameters
roi_period = [-(csus_delay+1),2];
baseline_period = [-2,-.5];
reward_period = [-.5,1];
lick_period = roi_period + lickrate_kernel.paddx;

%% selection criteria
rt_cutoffs = [-inf,+inf];

%% training stage settings
n_stages = 3;
stage_clrs = gray(n_stages+1);

%% data parsing

% preallocation
cs_plus_labels = cell(n_mice,1);

% iterate through mice
for mm = 1 : n_mice
    
    % parse mouse directory
    mouse_path = fullfile(data_path,mouse_ids{mm},experiment_id);
    mouse_dir = dir([mouse_path,filesep,'*',subexperiment_id]);
    mouse_dir = mouse_dir(cellfun(@(x)~contains(x,'.'),{mouse_dir.name}));
    
    % parse session directory
    session_ids = {mouse_dir.name};
    session_days = cellfun(@(x)str2double(strrep(x,'Day','')),session_ids);
    [~,chrono_idcs] = sort(session_days);
    session_ids = session_ids(chrono_idcs);
    n_sessions = numel(session_ids);
    
    % sort sessions chronologically
    days = cellfun(@(x) sscanf(x,'Day%i'),session_ids);
    [~,chrono_idcs] = sort(days);
    session_ids = session_ids(chrono_idcs);

    % initialize mouse counters
    mouse_reward_counter = 0;
    
    % iterate through sessions
    for ss = 1 : n_sessions
        progressreport(ss,n_sessions,sprintf(...
            'parsing behavioral & photometry data (mouse %i/%i)',mm,n_mice));
        session_path = fullfile(mouse_path,session_ids{ss});

        %% load behavioral data
        bhv_id = sprintf('%s_%s_eventlog.mat',...
            mouse_ids{mm},strrep(session_ids{ss},['_',subexperiment_id],''));
        bhv_path = fullfile(session_path,bhv_id);
        load(bhv_path)

        %% parse events
        event_labels = categorical(...
            eventlog(:,1),[15,16,5,10,0],...
            {'CS1','CS2','lick','reward','end'});
        event_times = eventlog(:,2);
        nosolenoid_flags = eventlog(:,3);
        event_idcs = (1 : numel(event_labels))';
        start_idx = find(ismember(event_labels,{'CS1','CS2'}),1);
        end_idx = find(event_labels == 'end');
        if isempty(end_idx)
            end_idx = numel(event_labels);
        end
        session_dur = event_times(end_idx);

        %% event selection
        valid_flags = ...
            ~isundefined(event_labels) & ...
            ~nosolenoid_flags & ...
            event_idcs >= start_idx & ...
            event_idcs < end_idx;
        n_events = sum(valid_flags);
        event_idcs = (1 : n_events)';
        event_labels = removecats(event_labels(valid_flags),'end');
        event_times = event_times(valid_flags);
        
        %% parse rewards
        reward_flags = event_labels == 'reward';
        reward_events = find(reward_flags);
        reward_times = event_times(reward_flags);
        n_rewards = sum(reward_flags);
        reward_idcs = (1 : n_rewards)';
        if n_rewards == 0
            continue;
        end

        %% parse CS+
        cs_flags = ismember(event_labels,{'CS1','CS2'});
        
        % preallocation
        cs_plus_flags = false(n_events,1);
        
        % iterate through rewards
        for ii = 1 : n_rewards
            cs_plus_idx = find(...
                cs_flags & event_idcs < reward_events(ii),1,'last');
            cs_plus_flags(cs_plus_idx) = true;
        end
        cs_plus_labels{mm,ss} = unique(event_labels(cs_plus_flags));
        csus_delays = round(reward_times - event_times(cs_plus_flags));
        if unique(csus_delays) ~= csus_delay
            continue;
        end
        
        %
        cs_times = event_times(cs_flags);
        cs_plus_times = ...
            cs_times(event_labels(cs_flags) == cs_plus_labels{mm,ss});
        cs_minus_times = ...
            cs_times(event_labels(cs_flags) ~= cs_plus_labels{mm,ss});
        n_cs_minus = numel(cs_minus_times);
        if n_cs_minus < n_rewards
            cs_minus_times = [cs_minus_times; nan(n_rewards-n_cs_minus,1)];
        elseif n_cs_minus > n_rewards
            cs_minus_times = cs_minus_times(1:n_rewards);
        end
        
        %% parse first licks after reward delivery
        lick_flags = event_labels == 'lick';
        
        % preallocation
        firstlick_flags = false(n_events,1);
        
        % iterate through rewards
        for ii = 1 : n_rewards
            firstlick_idx = find(...
                lick_flags & event_idcs > reward_events(ii),1,'first');
            firstlick_flags(firstlick_idx) = true;
        end
        firstlick_events = event_idcs(firstlick_flags);
        firstlick_times = event_times(firstlick_flags);
        firstlick_idcs = sum(reward_times > firstlick_times',2) + 1;
        firstlick_times = firstlick_times(firstlick_idcs);
        n_firstlicks = sum(firstlick_flags);

        %% compute inter-reward-intervals (IRI, nominal & actual)
        iri_nominal = diff([0;reward_times]);
        iri_actual = diff([0;event_times(firstlick_flags)]);
        iri_actual = iri_actual(firstlick_idcs);
        
        %% parse reaction time
        reaction_times = firstlick_times - reward_times;
        
        %% load photometry data
        photometry_path = fullfile(session_path,'Photometry.mat');
        load(photometry_path);
        
        %% parse licks
        lick_times = event_times(lick_flags);
        lick_edges = event_times(1) : dt : event_times(end);
        lick_counts = histcounts(lick_times,lick_edges);
        
        % get lick-aligned snippets of lick counts
        [lick_reward_snippets,~] = signal2eventsnippets(...
            lick_edges(1:end-1),lick_counts,reward_times,lick_period,dt);
        [lick_firstlick_snippets,lick_roi_time] = signal2eventsnippets(...
            lick_edges(1:end-1),lick_counts,firstlick_times,lick_period,dt);
        
        % nanify (maybe this should be moved inside??? the snippet fun???)
        time_mat = ...
            lick_roi_time > -[inf;diff(reward_times)] & ...
            lick_roi_time < +[diff(reward_times);inf];
        lick_reward_snippets(~time_mat) = nan;
        time_mat = ...
            lick_roi_time > -[inf;diff(firstlick_times)] & ...
            lick_roi_time < +[diff(firstlick_times);inf];
        lick_firstlick_snippets(~time_mat) = nan;
        
        %% compute inter-lick-intervals (ILI)
        ili_events = diff([nan;lick_times]);
        ili_idcs = sum(lick_times > reward_times',2);
        ili_rewards = arrayfun(@(x)ili_events(ili_idcs == x),reward_idcs,...
            'uniformoutput',false);
        
        %% parse photometry data
        
        % correct for nonstationary sampling frequency
        time = T(1) : dt : T(end);
        da = interp1(T,dff,time);
        
        % get event-aligned snippets of DA
        [da_baseline_snippets,da_baseline_time] = signal2eventsnippets(...
            time,da,firstlick_times,baseline_period,dt);
        [da_reward_snippets,da_reward_time] = signal2eventsnippets(...
            time,da,firstlick_times,reward_period,dt);
        [da_roi_snippets,~] = signal2eventsnippets(...
            time,da,reward_times,roi_period,dt);
        [da_csminus_snippets,da_roi_time] = signal2eventsnippets(...
            time,da,cs_minus_times,roi_period,dt);

        % preallocation
        da_response = nan(n_rewards,1);
        
        % iterate through rewards
        for ii = 1 : n_rewards
            
            % compute 'DA response' metric
            da_response(ii) = ...
                sum(da_reward_snippets(ii,:)) - sum(da_baseline_snippets(ii,:));
        end
        
        %% organize session data into tables
        
        % construct trial table
        index_table = table(...
            reward_idcs + mouse_reward_counter,...
            reward_idcs,...
            'variablenames',{...
            'mouse',...
            'session',...
            });
        
        % construct time table
        time_table = table(...
            reward_times,...
            firstlick_times,...
            'variablenames',{...
            'delivery',...
            'collection',...
            });
        
        % construct reward table
        reward_table = table(...
            index_table,...
            time_table,...
            'variablenames',{...
            'index',...
            'time',...
            });
        
        % construct IRI table
        iri_table = table(...
            iri_nominal,...
            iri_actual,...
            'variablenames',{...
            'nominal',...
            'actual',...
            });
        
        % construct lick table
        lick_table = table(...
            lick_reward_snippets,...
            lick_firstlick_snippets,...
            'variablenames',{'delivery','collection'});
        
        % construct DA table
        da_table = table(...
            da_roi_snippets,...
            da_csminus_snippets,...
            da_baseline_snippets,...
            da_reward_snippets,...
            da_response,...
            'variablenames',{'roi','cs-','baseline','reward','response'});
        
        % concatenate into a session table
        session_data = table(...
            categorical(cellstr(repmat(mouse_ids{mm},n_rewards,1)),mouse_ids),...
            repmat(ss,n_rewards,1),...
            reward_table,...
            iri_table,...
            reaction_times,...
            lick_table,...
            ili_rewards,...
            da_table,...
            'variablenames',{...
            'mouse',...
            'session',...
            'reward'...
            'iri',...
            'rt',...
            'licks',...
            'ili',...
            'da',...
            });
        
        % increment mouse counters
        mouse_reward_counter = mouse_reward_counter + n_rewards;
        
        % append to the current mouse's table
        if ss == 1
            mouse_data = session_data;
        else
            mouse_data = [mouse_data; session_data];
        end
    end
    
    % append to the final data table
    if mm == 1
        data = mouse_data;
    else
        data = [data;mouse_data];
    end
end

%% reward selection criteria
rt_flags = ...
    data.rt >= rt_cutoffs(1) & ...
    data.rt <= rt_cutoffs(2);
valid_flags = ...
    rt_flags;

%% reaction time bin settings
rt_binwidth = 1 / 15;
rt_roi = [roi_period(1),roi_period(2)];
rt_edges = rt_roi(1) : rt_binwidth : rt_roi(2);

%% plot reaction time distributions

% figure initialization
figure(...
    'windowstyle','docked',...
    'numbertitle','off',...
    'name','reaction_times',...
    'color','w');

% axes initialization
sps = gobjects(n_mice,1);
for ii = 1 : n_mice
    sps(ii) = subplot(ceil(n_mice/4),ceil(n_mice/2),ii);
end
set(sps,...
    'xlimspec','tight',...
    'xtick',0:100:max(data.reward.index.mouse),...
    'ylimspec','tight',...
    'nextplot','add',...
    'linewidth',2,...
    'fontsize',12,...
    'tickdir','out');

% iterate through mice
for mm = 1 : n_mice
    mouse_flags = data.mouse == mouse_ids{mm};
    
    % axes labels
    title(sps(mm),sprintf('%s_%s',mouse_ids{mm},unique([cs_plus_labels{mm,:}])),...
        'interpreter','none');
    xlabel(sps(mm),'Reward #');
    ylabel(sps(mm),'Reaction time (s)');
    
    % iterate through sessions
    n_sessions = max(data.session(mouse_flags));
    session_clrs = cool(n_sessions);
    for ss = 1 : n_sessions
        session_flags = data.session == ss;
        trial_flags = ...
            mouse_flags & ...
            session_flags & ...
            valid_flags;
        reaction_times = data.rt(trial_flags);
        plot(sps(mm),...
            data.reward.index.mouse(trial_flags),reaction_times,'.',...
            'markersize',7.5,...
            'color',session_clrs(ss,:));
        errorbar(sps(mm),...
            nanmean(data.reward.index.mouse(trial_flags)),...
            nanmedian(reaction_times),...
            quantile(reaction_times,.25)-nanmedian(reaction_times),...
            quantile(reaction_times,.75)-nanmedian(reaction_times),...
            'color',session_clrs(ss,:),...
            'marker','o',...
            'markersize',7.5,...
            'markeredgecolor','w',...
            'markerfacecolor',session_clrs(ss,:),...
            'linewidth',1.5,...
            'capsize',0);
    end
    trial_flags = ...
        mouse_flags & ...
        valid_flags;
    reaction_times = data.rt(trial_flags);
    ylim(sps(mm),quantile(reaction_times,[0,.95]));
end

